<?php 

// inisialisasi
$angkot1 = 1;

// kondisi terminasi
while ($angkot1 <= 10) {
    print "Angkot no.$angkot1 beroperasi dengan baik. <br>";
    $angkot1 += 1;
}

// for
print "<br>";
for ($angkot1 = 1; $angkot1 <= 10; $angkot1++) {
    print "Angkot no.$angkot1 beroperasi dengan baik. <br>";
}

?>